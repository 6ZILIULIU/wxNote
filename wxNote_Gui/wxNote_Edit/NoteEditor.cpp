#include "wxNote_Gui/wxNote_Edit/NoteEditor.h"

#include <QEvent>
#include <QKeyEvent>
#include <QFocusEvent>

//.._NoteEditor类实现

    /* 构造函数实现 */
    _NoteEditor::_NoteEditor(QWidget *_Parent)
        : QTextEdit(_Parent)
        {;}

    /////////////////////////////////////////////////////////////////////////
    //..protected部分

    /* keyPressEvent()事件重写 */
    void _NoteEditor::keyPressEvent(QKeyEvent *_Event)
        {
        switch (_Event->key())
            {
        case Qt::Key_Tab:
            if (_Event->modifiers() & Qt::ControlModifier)
                {
                _FinalTabWindowMove2NextTabSignal();
                break;
                }

        default:
            QTextEdit::keyPressEvent(_Event);
            }
        }

