//.._EliminateOrRestorePushButton类实现 */

    /* 构造函数实现 */
    _EliminateOrRestorePushButton
        ::_EliminateOrRestorePushButton(QWidget *_Parent)
        : QPushButton(_Parent)
        {
        setSizePolicy(QSizePolicy::Preferred, QSizePolicy::Preferred);
        }

    /////////////////////////////////////////////////////////////////////////
    //..protected部分

    /* sizeHint()函数重写 */
    QSize _EliminateOrRestorePushButton::sizeHint() const
        {
        QSize _CurrentSize = QPushButton::sizeHint();
        _CurrentSize.rheight() += 10;

        return _CurrentSize;
        }

